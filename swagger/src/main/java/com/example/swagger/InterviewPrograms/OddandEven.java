package com.example.swagger.InterviewPrograms;

public class OddandEven {
    public static void main(String[] a){

        for(int i = 0;i<=100;i++){
            if(i%2==0){
                System.out.println("Even Num"+" "+i);
            }
            if(i%2!=0){
                System.out.println("Odd Num"+" "+i);
            }
        }
    }
}
